#include <bits/stdc++.h>
using namespace std;

int tw[500005];
int main() {
    int n, b;
    cin >> n >> b;
    
    int c = 0;
    for (int i = 2; i <= n; i++) {
        if (tw[i]) continue;
        for (int j = 1; i * j <= n; j++) {
            tw[i*j] = 1;
        }
        c++;
    }

    cout << max(c - b, 0);
    
    return 0;
}
