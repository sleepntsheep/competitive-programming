#include <stdio.h>

#define mxn 300
#define mxppe 10000

unsigned char Plain[mxn][mxn];

int nx, ny;

enum Pipe {
    EMPTY,
    LEFTUP = 11,
    RIGHTUP = 12,
    LEFTDOWN = 13,
    RIGHTDOWN = 14,
    VERTSTR = 21,
    HORISTR = 22,
    PLUS = 31,
};
enum Dir {
    UP,
    LEFT,
    DOWN,
    RIGHT
};

int outofBound(int x, int y) {
    return (x < 0 || y < 0 || x >= nx || y >= ny);
}

int flood (int x, int y, int from) {
    if (outofBound(x, y)) return 0;
    if (Plain[y][x] == 0) return 0;

#ifdef DEBUG
    printf("X: %d Y: %d From: %d\n", x, y, from);
#endif
    int pipe = Plain[y][x];
    if (pipe != PLUS) Plain[y][x] = 0;
    switch (pipe) {
        case LEFTUP:
            if (from == LEFT) return 1 + flood(x, y-1, DOWN);
            else if (from == UP) return 1 + flood(x-1, y, RIGHT);
            break;
        case RIGHTUP:
            if (from == RIGHT) return 1 + flood(x, y-1, DOWN);
            else if (from == UP) return 1 + flood(x+1, y, LEFT);
            break;
        case RIGHTDOWN:
            if (from == RIGHT) return 1 + flood(x, y+1, UP);
            else if (from == DOWN) return 1 + flood(x+1, y, LEFT);
            break;
        case LEFTDOWN:
            if (from == LEFT) return 1 + flood(x, y+1, UP);
            else if (from == DOWN) return 1 + flood(x-1, y, RIGHT);
            break;
        case VERTSTR:
            if (from == UP) return 1 + flood(x, y+1, UP);
            else if (from == DOWN) return 1 + flood(x, y-1, DOWN);
            break;
        case HORISTR:
            if (from == RIGHT) return 1 + flood(x-1, y, RIGHT);
            else if (from == LEFT) return 1 + flood(x+1, y, LEFT);
            break;
        case PLUS:
            switch (from) {
                case UP:
                    Plain[y][x] = HORISTR;
                    return 1 + flood(x, y+1, UP);
                    break;
                case DOWN:
                    Plain[y][x] = HORISTR;
                    return 1 + flood(x, y-1, DOWN);
                    break;
                case LEFT:
                    Plain[y][x] = VERTSTR;
                    return 1 + flood(x+1, y, LEFT);
                    break;
                case RIGHT:
                    Plain[y][x] = VERTSTR;
                    return 1 + flood(x-1, y, RIGHT);
                    break;
            }
            break;
    }
    return 0;
}

int main() {
    // input
    scanf("%d%d", &ny, &nx);
    for (int i = 0; i < ny; i++)
        for (int j = 0; j < nx; j++)
            scanf(" %hhd", Plain[i]+j);

    int sp = 0;
    int length[mxppe] = { 0 };


    // top border
    for (int i = 0; i < nx; i++) {
        int e = Plain[0][i];
        if (e == LEFTUP || e == RIGHTUP || e == VERTSTR || e == PLUS) {
            length[sp] = flood(i, 0, UP);
            sp++;
        }
    }

    // left border
    for (int i = 0; i < ny; i++) {
        int e = Plain[i][0];
        if (e == LEFTDOWN || e == LEFTUP || e == HORISTR || e == PLUS){ 
            length[sp] = flood(0, i, LEFT);
            sp++;
        }
    }

    // right border
    for (int i = 0; i < ny; i++) {
        int e = Plain[i][nx-1];
        if (e == RIGHTUP || e == RIGHTDOWN || e == HORISTR || e == PLUS){
            length[sp] = flood(nx-1, i, RIGHT);
            sp++;
        }
    }

    // bottom border
    for (int i = 0; i < nx; i++) {
        int e = Plain[ny-1][i];
        if (e == LEFTDOWN || e == RIGHTDOWN || e == VERTSTR || e == PLUS) {
            length[sp] = flood(i, ny-1, DOWN);
            sp++;
        }
    }

    // output
    printf("%d\n", sp);
    for (int i = 0; i < sp; i++)
        printf("%d ", length[i]);

    return 0;
}

