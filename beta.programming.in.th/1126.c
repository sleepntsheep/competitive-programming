#include <stdio.h>
typedef long long ll;

#define N 5005

// in how many way can we make list
// a1+a2+...+aL = N when 1 <= a[i] <= K
//
// similar to coin change problem (?)
ll dp[N];
ll qs[N];

#define MAX(a,b) (((a) > (b)) ? (a) : (b))
#define CUBE(X) ((X)*(X)*(X))
int main() {
    ll i, j, n, k, a=0;
    *qs = *dp = 1;
    scanf("%lld%lld", &n, &k);
    for (i = 1; i <= n; i++) { // length of sequence = i
        if (i > 1) *qs = 0;
        for (j = 1; j <= n; j++)
            qs[j] = qs[j-1] + dp[j];
        for (j = 1; j <= n; j++) {
            dp[j] = qs[j-1];
            if (j-k-1 >= 0) dp[j] -= qs[j-k-1];
            dp[j] %= 2553;
        }
        a += CUBE(dp[n]);
    }
    printf("%lld", a%2553);
    return 0;
}

