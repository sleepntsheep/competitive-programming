#include <stdio.h>
#include <stdlib.h>

typedef struct pair {
    int s, n;
} P;

int
pcmp(const void *a, const void *b)
{
    return ((P*)a)->s - ((P*)b)->s;
}

int
main() 
{
    int n, q, i, w;
    scanf("%d%d", &n, &q);
#ifdef __cplusplus
    P *p = (P*)malloc(sizeof(P) * (n+1));
#else
    P *p = malloc(sizeof(P) * (n+1));
#endif
    for (i = 1; i <= n; i++) {
        scanf("%d", &p[i].s);
        p[i].n = i;
        p[i].s += p[i-1].s;
    }

    qsort(p+1, n, sizeof *p, pcmp);
    for (i = 1; i <= n; i++)
        if (p[i].n < p[i-1].n) p[i].n = p[i-1].n;

    while(q--) {
        scanf("%d", &w);
        P *l = p+1, *r = p + n, *mid, *ans = p;
        while (l <= r) {
            mid = l + (r - l + 1) / 2;
            if (mid->s <= w) {
                ans = mid;
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        printf("%d\n", ans->n);
    }
    return 0;
}
