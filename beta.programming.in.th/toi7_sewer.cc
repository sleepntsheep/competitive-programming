#include <bits/stdc++.h>
using namespace std;
#define mxn 105

int a, b;
char A[mxn][mxn];
int V[mxn][mxn];
vector<pair<int,int>> adj[mxn][mxn];

bool out(int x, int y){
    return (x <= 0 || y <= 0 || x > b || y > a);
}

int main() {
    cin.tie(0);
    ios::sync_with_stdio(0);
    cin >> a >> b;
    for (int i = 1; i <= a; i++) {
        for (int j = 1; j <= b; j++) {
            cin >> A[i][j];
            if (A[i][j] == 'R' || A[i][j] == 'B') {
                adj[i][j].push_back({i,j+1});
                adj[i][j+1].push_back({i,j});
            }
            if (A[i][j] == 'D'|| A[i][j] == 'B') {
                adj[i][j].push_back({i+1,j});
                adj[i+1][j].push_back({i,j});
            }
        }
    }

    queue<pair<int, int>> q;
    V[1][1] = 1;
    q.push({1, 1});
    int c = 1;
    while (!q.empty()) {
        int size = q.size();
        c++;
        while(size--) {
            int x = q.front().first;
            int y = q.front().second;
            q.pop();

            for (auto pr : adj[y][x]) {
                int nx = pr.second;
                int ny = pr.first;
                if (out(nx, ny)) continue;
                if (V[ny][nx] != c && V[ny][nx]) {
                    continue;
                }
                else if (V[ny][nx] == c) {
                    cout << c << '\n' << ny << ' ' << nx;
                    return 0;
                }
                else {
                    V[ny][nx] = c;
                    q.push({nx, ny});
                }
            }
        }
    }


    return 0;
}
