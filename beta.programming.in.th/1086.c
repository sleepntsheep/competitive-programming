#include <stdio.h>
#include <stdlib.h>

#define FOR(i,a,b) for(int i = a; i < b; i++)
#define ll long long
#define INF 1e9
#define MAX(a,b) ((a) > (b) ? (a) : (b))
#define MIN(a,b) ((a) < (b) ? (a) : (b))

int pi[10005], at, n, k, a, i, j, s, nread, start;
int main() {
    scanf("%d%d%d", &n, &k, &a);

    for(i = 0; i < n; i++)  {
        scanf("%d", &s);
        for (j = at; j < at + s; j++)
            pi[j] = i;
        at += s;
    }

    for (i = 0; i < at; i++) {
        int rd[105] = { 0 };
        int c = 0, page = i;
        for (int tp = 0; tp < k && page < at; tp++) {
            c += !rd[pi[page]];
            rd[pi[page]] = 1;
            page += a;
        }

        if (c > nread) {
            nread = c;
            start = i;
        }
    }

    printf("%d %d", start+1, nread);

    return 0;
}
