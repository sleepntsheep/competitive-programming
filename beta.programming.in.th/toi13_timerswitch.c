#include <stdio.h>
#include <stdlib.h>

#define B 13

int _hash(char *s){
    int ret=0;
    for(;*s;s++){
        ret*=B;
        ret+=*s-'0';
    }
    return ret;
}

int _pow(int b, int n){
    int ret = 1;
    for(int i=0;i<n;i++)
        ret*=b;
    return ret;
}

int main() {
    int sn;
    scanf("%d",&sn);
    char s[sn+5];
    scanf("%s",s);

    int sh = _hash(s);

    int ssh = sh;

    int lts = _pow(B,sn-1);

    for(int t=0;t<sn;t++){
        ssh -= (s[t]-'0') * lts;
        ssh *= B;
        ssh += s[t]-'0';
        if(sh == ssh){
            printf("%d", t+1);
            return 0;
        }
    }

    return 0;
}

