#include <bits/stdc++.h>
using namespace std;

const int INF = 1e9;
const int N = 100005;

#define pii pair<int, int>
#define st first
#define nd second
#define ll long long

int c[N];
pii p[N];

int main() {
    cin.tie(nullptr);
    ios::sync_with_stdio(false);
    int n, q;
    cin >> n >> q;
    for (int i = 1; i <= n; i++) {
        cin >> p[i].st;
        p[i].st += p[i-1].st;
        p[i].nd = i;
    }
    sort(p+1,p+n+1);
    sort(c+1,c+n+1);

    for (int i = 1; i <= n; i++) {
        c[i] = p[i].st;
        p[i].nd = max(p[i].nd, p[i-1].nd);
    }
    while(q--) {
        int qs;
        cin >> qs;
        cout << p[upper_bound(c+1,c+n+1,qs)-c-1].nd << '\n';
    }
    return 0;
}
