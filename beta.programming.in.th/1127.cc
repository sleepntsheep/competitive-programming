#include <stdio.h>

int main() {
    int n, mn = 1e9, mi;
    int cost[10] = {0};
    scanf("%d", &n);
    for (int i = 1; i < 10; i++) {
        scanf("%d", cost+i);
        if (mn > cost[i]){ 
            mn = cost[i];
            mi = i;
        }
    }

    int mdt = n / mn;
    char s[mdt+1];
    for (int i = 0; i < mdt; i++) {
        s[i] = mi + '0';
    }
    n %= mn;

    for (int i = 0; i < mdt; i++) {
        for (int j = 9; j && j > mi; j--) {
            if (n >= cost[j] - mn) {
                s[i] = j + '0';
                n -= cost[j] - mn;
                break;
            }
        }
    }

    s[mdt] = 0;
    puts(s);

    return 0;
}
