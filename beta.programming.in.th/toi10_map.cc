struct pt {
    int l, u, d, r;
};

int B[600][600];

pt D[200*200];

void rcs(int d, int x, int y) {
    B[y][x] = d;
    {
        if (D[d].l!=-1)
            if(-1==B[y][x-1])
                rcs(D[d].l, x-1, y);
    }
    {
        if (D[d].r!=-1)
            if(-1==B[y][x+1])
                rcs(D[d].r, x+1, y);
    }{
        if (D[d].d!=-1)

            if(-1==B[y+1][x])
                rcs(D[d].d, x, y+1);
    }{
        if (D[d].u!=-1)
            if (-1==B[y-1][x])
                rcs(D[d].u, x, y-1);
    }
}

